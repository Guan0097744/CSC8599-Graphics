#include "PBRCubemap.h"

PBRCubemap::PBRCubemap() {}

void PBRCubemap::Allocate(GLenum format1, GLenum format2, GLuint width, GLuint height, GLenum type, GLint param1, GLint param2)
{
    for (unsigned int i = 0; i < 6; i++)
    {
        glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i,
            0, format1, width, height, 0, format2, type, NULL);
    }

    //glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR_MIPMAP_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, param1);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, param2);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
}

void PBRCubemap::Generate()
{
	glGenTextures(1, &id);
}

void PBRCubemap::Bind()
{
    glBindTexture(GL_TEXTURE_CUBE_MAP, id);
}

